public class OperadoresMatematicos {
    public static void main(String[] args) {
        int valor = 0;
        System.out.println(--valor); //valor = valor + 1
        System.out.println(valor);
        valor+=10;
        System.out.println(valor);
        System.out.println(valor%3);
        for (int i=0;1<10;i++){
            System.out.println(i);
        }
        
    }
}
