package br.edu.ifrn.correcao_prova2;

/**
 *
 * @author joaon
 */
public class Raca {
    private int id;
    private String origem;
    private double tamanhoMaximo;
    private double tamanhoMinimo;
    private int expectativaVida;
    private int temperamento;

    /**
     * @return the id
     */
    public int getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(int id) {
        this.id = id;
    }

    /**
     * @return the origem
     */
    public String getOrigem() {
        return origem;
    }

    /**
     * @param origem the origem to set
     */
    public void setOrigem(String origem) {
        this.origem = origem;
    }

    /**
     * @return the tamanhoMaximo
     */
    public double getTamanhoMaximo() {
        return tamanhoMaximo;
    }

    /**
     * @param tamanhoMaximo the tamanhoMaximo to set
     */
    public void setTamanhoMaximo(double tamanhoMaximo) {
        this.tamanhoMaximo = tamanhoMaximo;
    }

    /**
     * @return the tamanhoMinimo
     */
    public double getTamanhoMinimo() {
        return tamanhoMinimo;
    }

    /**
     * @param tamanhoMinimo the tamanhoMinimo to set
     */
    public void setTamanhoMinimo(double tamanhoMinimo) {
        this.tamanhoMinimo = tamanhoMinimo;
    }

    /**
     * @return the expectativaVida
     */
    public int getExpectativaVida() {
        return expectativaVida;
    }

    /**
     * @param expectativaVida the expectativaVida to set
     */
    public void setExpectativaVida(int expectativaVida) {
        this.expectativaVida = expectativaVida;
    }

    /**
     * @return the temperamento
     */
    public int getTemperamento() {
        return temperamento;
    }

    /**
     * @param temperamento the temperamento to set
     */
    public void setTemperamento(int temperamento) {
        this.temperamento = temperamento;
    }
    
    
}
